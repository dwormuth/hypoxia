      SUBROUTINE SETNGO
C
C     REMOVE SOME REDUNDANT CODE FROM THE MAIN.
C
      CHARACTER*4 NAME
      COMMON ICOM1(176), NAME(7),ISTUDY,IDATE(3),IFLAG(75),NVAR,NTABLE,
     A   PARAM(75),  ICOM2(96)
C
      DO 50  J = 1,NVAR
      IF (IABS(IFLAG(J)) .NE. 3)  GO TO 50
      IFLAG (J) = 0
      PARAM (J) = -1.0
   50 CONTINUE
      CALL GT CHRT
      RETURN
      END
