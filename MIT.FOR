      SUBROUTINE MIT (ISUB,NKNOWN)
C
C     'CONDENSED' CHART SUBROUTINE FOR HYPOXIA.  CHARTS 21-23
C
      REAL K
      CHARACTER*4 NAME
      COMMON ICOM1(176), NAME(7),ISTUDY,IDATE(3),IFLAG(75),NVAR,NTABLE,
     A   PARAM(75),  ICOM2(96)
      EQUIVALENCE (A PCO2,PARAM(4)), (RQ,PARAM(5)), (VO2,PARAM(15)),
     A   (Q,PARAM(16)), (A O2 TOT,PARAM(36)), (O2 DEL,PARAM(51)),
     B   (COD,PARAM(52)), (VCO2,PARAM(53)), (AL VENT,PARAM(55)),
     C   (VPR,PARAM(56))
C
      DATA  K /0.863/
C
      ISUB20 = ISUB - 20
      GO TO (2100,2200,2300), ISUB20
C
C***************************************
C     SUBROUTINE OXY DEL   CHART 21
C     OXYGEN DELIVERY & COEFF. EQNS., FROM MITHEOFER
C
C     O2 DELIVERY = ART O2 CONC * Q
C     COEFF. OF O2 DELIVERY = O2 DELIVERY / VO2
C
 2100 O2 DEL = A O2 TOT * 10.0 * Q
      COD = O2 DEL / VO2
      IFLAG (51) = 3
      IFLAG (52) = 3
      GO TO 9900
C
C***************************************
C     SUBROUTINE RQ O2 CO2    CHART 22
C     RQ, O2 & CO2 RELATIONSHIPS, FROM MITHOEFER
C
C     R.Q. = CO2 RELEASE / O2 UPTAKE   (STPD)
C
 2200 GO TO (2210,2220,2230),  NKNOWN
 2210 RQ = VCO2 / VO2
      IFLAG (5) = 3
      GO TO 9900
C
 2220 VO2 = VCO2 / RQ
      IFLAG (15) = 3
      GO TO 9900
C
 2230 VCO2 = RQ * VO2
      IFLAG (53) = 3
      GO TO 9900
C
C***************************************
C     SUBROUTINE VENTILATION    CHART 23
C     EFFECTIVE ALVEOLOAR VENTILATION EQNS, FROM MITHOEFER
C
C     ALV VENT = K * CO2 RELEASE / ART PCO2
C        WHERE VCO2 IS IN STPD
C
C     VENTILATION-PERFUSION RATIO = ALV VENT / CARDIAC OUTPUT
C
 2300 AL VENT = K * VCO2 / A PCO2
      VPR = AL VENT / Q
      IFLAG (55) = 3
      IFLAG (56) = 3
C
 9900 RETURN
      END
