      PROGRAM HYPDAT
      INTEGER FILE
C
      DATA MAX /11/
      DATA FILE /50/
C
  107 FORMAT (32X,'-999',44X)
  117 FORMAT (80X)
C
      OPEN (FILE,FILE='HYPOX.DAT',STATUS='NEW',ACCESS='DIRECT',
     A   FORM='FORMATTED',RECL=80)
C
C
      LMNO = 1
      DO 100  I = 1,MAX
      WRITE (FILE,107,REC=LMNO)
      LMNO = LMNO + 1
      DO 50  J = 1,8
      WRITE (FILE,117,REC=LMNO)
      LMNO = LMNO + 1
   50 CONTINUE
  100 CONTINUE
C
      CLOSE (FILE,STATUS='KEEP')
      STOP
      END
